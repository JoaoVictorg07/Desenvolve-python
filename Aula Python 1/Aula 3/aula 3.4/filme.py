avaliação = int(input("Escreva sua avaliação do filme: "))

if avaliação == 5:
    print("Excelente!")
elif avaliação == 4:
    print("Muito Bom!")  
elif avaliação == 3:
    print("Bom!")  
elif avaliação == 2:
    print("Regular!")  
elif avaliação == 1:
    print("Ruim!")  
else:
    print("Avaliação inválida. Por favor, insira um número de 1 a 5.")   
    